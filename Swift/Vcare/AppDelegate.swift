    // MARK: - Auto login func
    func autoLogin() {
        var initialVC: UIViewController
        let storyboard = UIStoryboard(name: "Main", bundle: nil)
        if Auth.auth().currentUser != nil {
            initialVC = storyboard.instantiateViewController(withIdentifier: IDENTIFIRE_TABBAR)
        } else {
            initialVC = storyboard.instantiateViewController(withIdentifier: IDENTIFIRE_WELCOME)
        }
        window?.rootViewController = initialVC
        window?.makeKeyAndVisible()
    }

